                <label class="titulo">PROPÓSITO DEL HUERTO</label>
            </h2>
            <div style="text-align: justify;" class="contenidoSeccion container">
                <p>El Huerto Ibero surge como una iniciativa del <strong>Departamento de Arquitectura, Urbanismo e Ingeniería Civil,</strong> así mismo forma parte de los colaboradores del <strong>Instituto de Investigación Aplicada y Tecnología <a href="https://iniat.ibero.mx/" target="_blank"><abbr title="Instituto de Investigación Aplicada y Tecnología">(InIAT) </abbr></a></strong> encargado de promover el bienestar a través de la transformación tecnológica. </p>
                <p>Se realiza investigación inter y transdisciplinar que aporta a la educación ambiental en temas de agricultura urbana y la mejora de procesos productivos de cultivos urbanos con el acompañamiento de tecnología.</p>
                <p>El Huerto Ibero busca ser un referente en investigación sobre el impacto socioeconómico, en salud y en el medio ambiente que tiene un espacio verde productivo dentro de una institución académica.</p>
                <p>Quiere ser un referente en impulsar la adopción de la agricultura urbana en comunidades universitarias y la sociedad. </p>
                <p>Busca ser un ejemplo de la integración de técnicas agroecológicas tradicionales y tecnologías de automatización y ciencia de datos para la mejora en la producción de alimentos.</p>
